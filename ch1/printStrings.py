#print famous quote
famous_name = "  Albert  Einstein  "
print(f"{famous_name} once said, \"A person who never made a mistake never tried anything new.\" ")
#print white spaces
print(f"{famous_name} once said, \"A person who never made a mistake never tried anything new.\" ")

#print name on new line
print(f"{famous_name} once said,\n \t \"A person who never made a mistake never tried anything new.\" ")

print(f"{famous_name.lstrip()} once said,\n \"A person who never made a mistake never tried anything new.\" 1")

print(f"{famous_name.rstrip()} once said,\n \"A person who never made a mistake never tried anything new.\" 2")

print(f"{famous_name.strip()} once said,\n \"A person who never made a mistake never tried anything new.\" 3")